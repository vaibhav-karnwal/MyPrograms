#for adding two numbers
print(2+3)

#for subtracting two numbers
print(2-3)                                          #PRECEDECE RULE
                                    #OPERATORS            |           precedence and associative rule
#for float division                 #Paranthese           |           Highest
print(2/3)                          #Exponent             |           Right to left
                                    #*,/,//,%              |           left to right
#for multiplication                 #+,-                  |           left to right
print(2*3)

#for integer division
print(2//3)

#for modulo,it gives remainder
print(2%3)

#for Exponent
print(2**3)

#round Function 
print(round(2**3, 4))

#Examples of precedence and associative rule
print(2**3/2*6-4*(3-4/2))
print((2+3)/2)
print((2+3)*5/2%6)

